<template>
  <a
    v-if="to.match(/^((ftp|http(s)?):\/\/|(mailto):)/)"
    class="inline-flex items-center justify-center rounded"
    :class="{ 'text-link-dark dark:text-link-bright': isColored }"
    :href="to"
    :rel="
      [...(nofollow ? ['nofollow'] : []), 'noopener', 'noreferrer'].join(' ')
    "
    target="_blank"
    @click="$emit('click')"
  >
    <slot />
  </a>
  <nuxt-link
    v-else
    :append="append"
    class="inline-flex items-center justify-center rounded"
    :class="{ 'text-link-dark dark:text-link-bright': isColored }"
    :to="to"
    @click.native="$emit('click')"
  >
    <slot />
  </nuxt-link>
</template>

<script lang="ts">
import { defineComponent } from '#app'

export default defineComponent({
  props: {
    append: {
      default: false,
      type: Boolean,
    },
    isColored: {
      default: true,
      type: Boolean,
    },
    nofollow: {
      default: false,
      type: Boolean,
    },
    to: {
      required: true,
      type: String,
    },
  },
})
</script>
